        'id', 'nama', 'bentuk', 'satuan', 'dosis', 'kategori', 'stok'
    ];
    /*
    |--------------------------------------------------------------------------
    | FUNCTIONS
    |--------------------------------------------------------------------------
    */

    /*
    |--------------------------------------------------------------------------
    | RELATIONS
    |--------------------------------------------------------------------------
    */
    public function reseps()
    {
        return $this->hasMany(Resep::class, 'obat_id');  // Ensure this is the correct foreign key
    }
